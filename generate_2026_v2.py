def set_cell(cell, text, font_name='宋体', size=Pt(9), bold=False, align='left'):
    cell.text = ''
    p = cell.paragraphs[0]
    run = p.add_run(text)
    run.font.name = font_name
    run.font.size = size
    run.bold = bold
    run.element.rPr.rFonts.set(qn('w:eastAsia'), font_name)
    if align == 'center':
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER

